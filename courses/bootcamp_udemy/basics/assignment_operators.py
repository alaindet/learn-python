"""
Python does not have prefix (++i) and postfix (i++) operators
There are only short-hand assignments like below
"""

a = 1
a += 2 # Equivalent: a = a + 2
a -= 1 # Equivalent: a = a - 1
a *= 3 # Equivalent: a = a * 3
a /= 2 # Equivalent: a = a / 2
a **= 2 # Equivalent: a = a ** 2
a %= 2 # Equivalent: a = a % 2
